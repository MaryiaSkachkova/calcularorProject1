package kh.maryiaskachkova.calcapp.util;

public class Calculator {
    public static int calcSum(int num1, int num2) {
        int sum = num1 + num2;
        return sum;
    }

    public static int calcDifference(int num1, int num2) {
        int difference = num1 - num2;
        return difference;
    }

    public static int calcMultiply(int num1, int num2) {
        int multyply = num1 * num2;
        return multyply;
    }

}
